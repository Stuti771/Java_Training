package lecture1;

public class DataType {
    public static void main(String[] args) {
        byte a=5;
        short b=8;
        int c=10;
        float f=20.345f;
        boolean data=false;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(f);
        System.out.println(data);
    }
}
